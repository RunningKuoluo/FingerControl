void USART_Config(void)
{
	GPIO_InitTypeDef GPIO_InitStructure;
	USART_InitTypeDef USART_InitStructure;

		// 打开串口 GPIO 的时钟
	DEBUG_USART_GPIO_APBxClkCmd(DEBUG_USART_GPIO_CLK, ENABLE);
	 
	// 打开串口外设的时钟
	DEBUG_USART_APBxClkCmd(DEBUG_USART_CLK, ENABLE);

	// 将 USART Tx 的 GPIO 配置为推挽复用模式
	GPIO_InitStructure.GPIO_Pin = DEBUG_USART_TX_GPIO_PIN;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_AF_PP;
	GPIO_InitStructure.GPIO_Speed = GPIO_Speed_50MHz;
	GPIO_Init(DEBUG_USART_TX_GPIO_PORT, &GPIO_InitStructure);

	// 将 USART Rx 的 GPIO 配置为浮空输入模式
	GPIO_InitStructure.GPIO_Pin = DEBUG_USART_RX_GPIO_PIN;
	GPIO_InitStructure.GPIO_Mode = GPIO_Mode_IN_FLOATING;
	GPIO_Init(DEBUG_USART_RX_GPIO_PORT, &GPIO_InitStructure);

	// 配置串口的工作参数
	// 配置波特率
	USART_InitStructure.USART_BaudRate = DEBUG_USART_BAUDRATE;
	// 配置 针数据字长
	USART_InitStructure.USART_WordLength = USART_WordLength_8b;
	// 配置停止位
	USART_InitStructure.USART_StopBits = USART_StopBits_1;
	// 配置校验位
	USART_InitStructure.USART_Parity = USART_Parity_No ;
	// 配置硬件流控制
	USART_InitStructure.USART_HardwareFlowControl =
	USART_HardwareFlowControl_None;
	// 配置工作模式，收发一起
	USART_InitStructure.USART_Mode = USART_Mode_Rx | USART_Mode_Tx;
	// 完成串口的初始化配置
	USART_Init(DEBUG_USARTx, &USART_InitStructure);

//	// 串口中断优先级配置
//	NVIC_Configuration();

//	// 使能串口接收中断
//	USART_ITConfig(DEBUG_USARTx, USART_IT_RXNE, ENABLE);

	// 使能串口
	USART_Cmd(DEBUG_USARTx, ENABLE);
}

/*发送一个字节函数*/
void Usart_SendByte(USART_TypeDef* pUSARTx,uint8_t data)
{
	/*发送数据函数*/
	USART_SendData(pUSARTx,data);
	
	/*如果发送数据为空一直停在这等待，如果为SET=1则跳出等待，表示一个字节已经发送成功*/
	while( USART_GetFlagStatus(pUSARTx, USART_FLAG_TXE) == RESET );
	
}

/*发送两个字节函数*/
void Usart_SendHalfWord(USART_TypeDef* pUSARTx,uint16_t data)
{
	uint8_t temp_h,temp_l;
	
	/*取出高八位*/
	temp_h = (data&0xff00) >> 8;
	
	/*取出低八位*/
	temp_l = data&0xff;
	
	/*发送数据函数*/
	/*发送高八位数据*/
	USART_SendData(pUSARTx,temp_h);
	
	/*如果发送数据为空一直停在这等待，如果为SET=1则跳出等待，表示一个字节已经发送成功*/
	while( USART_GetFlagStatus(pUSARTx, USART_FLAG_TXE) == RESET );
	
	/*发送低八位数据*/
	USART_SendData(pUSARTx,temp_l);
	
	/*如果发送数据为空一直停在这等待，如果为SET=1则跳出等待，表示一个字节已经发送成功*/
	while( USART_GetFlagStatus(pUSARTx, USART_FLAG_TXE) == RESET );
	
}

/*发送8位字节数组的函数*/
void Usart_SendArray(USART_TypeDef* pUSARTx,uint8_t *array,uint8_t num)
{
	uint8_t i;
	
	for( i=0; i<num; i++)
	{
		Usart_SendByte( pUSARTx, array[i]);
	}
  
	
	/*如果发送数据为空一直停在这等待，USART_FLAG_TC发送完成寄存器如果为SET=1则跳出等待，表示数据已经发送成功*/
	while( USART_GetFlagStatus(pUSARTx, USART_FLAG_TC) == RESET );
	
}

/*发送字符串的函数*/
void Usart_SendStr(USART_TypeDef* pUSARTx,uint8_t *str)
{
	/*必须首地址初始化*/
	uint8_t i = 0;
	do
	{
		Usart_SendByte(pUSARTx, *(str+i));
		i++;
	}while(*(str+i) != '\0');
	/*如果发送数据为空一直停在这等待，USART_FLAG_TC发送完成寄存器如果为SET=1则跳出等待，表示数据已经发送成功*/
	while( USART_GetFlagStatus(pUSARTx, USART_FLAG_TC) == RESET );
	
}

///重定向 c 库函数 printf 到串口，重定向后可使用 printf 函数
 int fputc(int ch, FILE *f)
{
	/* 发送一个字节数据到串口 */
		USART_SendData(DEBUG_USARTx, (uint8_t) ch);

	 /* 等待发送完毕 */
	while (USART_GetFlagStatus(DEBUG_USARTx, USART_FLAG_TXE) == RESET);
	 
	return (ch);
}
	 
///重定向 c 库函数 scanf 到串口，重写向后可使用 scanf、getchar 等函数
	int fgetc(FILE *f)
{
	/* 等待串口输入数据 */
	while (USART_GetFlagStatus(DEBUG_USARTx, USART_FLAG_RXNE) == RESET);

	return (int)USART_ReceiveData(DEBUG_USARTx);
 }
